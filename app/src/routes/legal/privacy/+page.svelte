	<h2>Privacy Policy</h2>
	<p class="meta">Last updated: 18 May 2026 · Effective for users of Muse (lovemuse.app).</p>

	<p>
		Muse ("we", "us", "our") respects your privacy. This Privacy Policy explains
		what personal data we collect, how we use it, who we share it with, and the
		rights you have. It is written to comply with the
		<strong>Personal Data (Privacy) Ordinance (Cap. 486)</strong> of Hong Kong
		("PDPO") and the six Data Protection Principles ("DPPs") issued by the
		Office of the Privacy Commissioner for Personal Data, Hong Kong ("PCPD").
	</p>

	<h3>1. Who we are (Data User)</h3>
	<p>
		Muse is operated as an online service available at lovemuse.app and via
		mobile applications. For the purposes of the PDPO, we act as the
		<em>Data User</em> in respect of the personal data we collect from you.
		Contact details for privacy enquiries are set out at the end of this
		Policy.
	</p>

	<h3>2. Personal data we collect</h3>
	<p>We collect only the data we need to run the service:</p>
	<ul>
		<li><strong>Account data:</strong> email address, display name, password (stored hashed), language preference.</li>
		<li><strong>Profile data (optional):</strong> bio, city, dietary preferences, favourite cuisine, Instagram handle, website, phone, avatar.</li>
		<li><strong>Content you create:</strong> restaurants you pin as "visited" or "to visit", ratings, comments, shared lists.</li>
		<li><strong>Social graph:</strong> friend connections and invitations you send or accept.</li>
		<li><strong>Device & technical data:</strong> IP address, device type, operating system, app version, crash logs, approximate or precise location <em>only</em> when you explicitly grant the permission.</li>
		<li><strong>Communications:</strong> emails you send to us at <a href="mailto:info@lovemuse.app">info@lovemuse.app</a>.</li>
	</ul>
	<p>
		We do not knowingly collect data from individuals under the age of 13. If
		you believe a minor has provided us with personal data, contact us and we
		will delete it.
	</p>

	<h3>3. Purposes of collection (DPP 1 & 3)</h3>
	<p>We collect and use your personal data for the following purposes:</p>
	<ul>
		<li>Creating, maintaining and securing your account.</li>
		<li>Letting you pin, rate and share restaurants with friends.</li>
		<li>Showing you your friends' activity in a personalised feed.</li>
		<li>Sending transactional emails (invitations, password resets, security alerts).</li>
		<li>Improving the service, debugging, and preventing abuse.</li>
		<li>Complying with applicable laws and lawful requests from authorities.</li>
	</ul>
	<p>
		We will not use your personal data for a new purpose that is materially
		different from those listed above without first obtaining your express and
		voluntary consent, as required by DPP 3.
	</p>

	<h3>4. How we share data (DPP 3)</h3>
	<p>
		We do <strong>not</strong> sell your personal data. We share it only in the
		following circumstances:
	</p>
	<ul>
		<li><strong>With other users:</strong> your display name, avatar, city, pins, ratings and shared lists are visible to friends; shared lists you generate are accessible to anyone with the public link.</li>
		<li><strong>With service providers (Data Processors):</strong> hosting (AWS, EC2/RDS in the United States), transactional email (Resend), mapping & places data (Google Maps Platform, OpenStreetMap/Nominatim via our backend proxy). They process data on our instructions under contractual safeguards.</li>
		<li><strong>For legal reasons:</strong> when required by a court order, subpoena or other lawful request, including from Hong Kong authorities under the PDPO or other ordinances.</li>
	</ul>

	<h3>5. Cross-border transfer</h3>
	<p>
		Although Section 33 of the PDPO (regulating transfers of personal data
		outside Hong Kong) is not yet in force, we apply its spirit voluntarily.
		Where we transfer data to processors outside Hong Kong (for example, to AWS
		data centres in the United States or to Resend in the European Union /
		United States), we ensure that:
	</p>
	<ul>
		<li>the recipient is bound by written contractual terms to protect the data to a standard substantially similar to the PDPO;</li>
		<li>the data is transmitted using TLS encryption in transit; and</li>
		<li>the transfer is limited to what is necessary to provide the service.</li>
	</ul>

	<h3>6. Retention (DPP 2)</h3>
	<p>
		We keep personal data only for as long as is necessary for the purposes set
		out above. When you delete your account, we delete or anonymise your
		personal data within 30 days, except where retention is required to comply
		with a legal obligation, to resolve disputes or to enforce our agreements.
		Backups containing residual copies are purged on a rolling 90-day cycle.
	</p>

	<h3>7. Security (DPP 4)</h3>
	<p>
		We apply reasonable and practicable security measures, including TLS 1.2+
		for all traffic, hashed password storage (Django's PBKDF2 default), access
		controls on our infrastructure, rate-limiting on sensitive endpoints, and
		logging. No system is perfectly secure; if we become aware of a personal
		data breach that is likely to result in real risk of significant harm, we
		will notify affected users and, where appropriate, the PCPD without undue
		delay.
	</p>

	<h3>8. Accuracy (DPP 2)</h3>
	<p>
		You can edit most of your personal data directly from your profile screen.
		If any data we hold is inaccurate, please correct it in-app or write to us.
	</p>

	<h3>9. Your rights (DPP 5 & 6)</h3>
	<p>Under the PDPO, you have the right to:</p>
	<ul>
		<li>be informed of our privacy policies and practices (DPP 5);</li>
		<li>ascertain whether we hold personal data about you and to request a copy of that data (Data Access Request, s.18 PDPO);</li>
		<li>request correction of inaccurate data (Data Correction Request, s.22 PDPO);</li>
		<li>opt out of any direct marketing (we currently send no direct marketing).</li>
	</ul>
	<p>
		To exercise any of these rights, email
		<a href="mailto:info@lovemuse.app">info@lovemuse.app</a> from the address
		linked to your account. We will respond within 40 days, as required by the
		PDPO. We may charge a reasonable fee for compliance with a Data Access
		Request, in line with PCPD guidance.
	</p>

	<h3>10. Complaints</h3>
	<p>
		If you believe we have failed to comply with the PDPO, you may lodge a
		complaint with the Office of the Privacy Commissioner for Personal Data,
		Hong Kong (PCPD), 12/F, Sunlight Tower, 248 Queen's Road East, Wanchai,
		Hong Kong — <a href="https://www.pcpd.org.hk" rel="noopener" target="_blank">www.pcpd.org.hk</a>.
	</p>

	<h3>11. Changes to this Policy</h3>
	<p>
		We may update this Policy. Material changes will be notified in-app or by
		email at least 14 days before they take effect. Continued use after the
		effective date constitutes acceptance.
	</p>

	<h3>12. Contact</h3>
	<p>
		For any privacy-related question or request, contact us at
		<a href="mailto:info@lovemuse.app">info@lovemuse.app</a>.
	</p>
