	<h2>Cookie Policy</h2>
	<p class="meta">Last updated: 18 May 2026</p>

	<p>
		This Cookie Policy explains how Muse uses cookies and similar storage
		technologies (collectively, "cookies") on the lovemuse.app website and
		within the Muse mobile applications. It supplements our
		<a href="/legal/privacy">Privacy Policy</a> and reflects the guidance of
		the Office of the Privacy Commissioner for Personal Data, Hong Kong on
		the use of online tracking technologies under the
		<strong>Personal Data (Privacy) Ordinance (Cap. 486)</strong>.
	</p>

	<h3>1. What is a cookie?</h3>
	<p>
		A cookie is a small text file stored on your device when you visit a
		website. We also use related technologies such as <em>localStorage</em>,
		<em>sessionStorage</em> and the Capacitor Preferences API on mobile. For
		simplicity we refer to all of them as "cookies".
	</p>

	<h3>2. Cookies we use</h3>
	<p>We use a small number of cookies, all of which fall into the
	"strictly necessary" or "preference" categories. We do <strong>not</strong>
	use advertising cookies or cross-site tracking pixels.</p>

	<h3>2.1 Strictly necessary</h3>
	<ul>
		<li><strong>Authentication tokens</strong> (JWT access & refresh) — keep you logged in. Stored in <em>localStorage</em> on the web and in secure native storage on mobile. Without these the Service cannot function.</li>
		<li><strong>CSRF token</strong> — protects sensitive backend endpoints from cross-site request forgery.</li>
	</ul>

	<h3>2.2 Preferences</h3>
	<ul>
		<li><strong>Language preference</strong> (<code>locale</code>) — remembers whether you chose English, Spanish or Italian.</li>
		<li><strong>UI state</strong> — last-used filter on the map and feed, list of dismissed onboarding hints.</li>
	</ul>

	<h3>2.3 Third-party</h3>
	<p>
		We integrate two third-party services that may set their own cookies when
		you actively use their features:
	</p>
	<ul>
		<li><strong>Google Maps Platform</strong> — when a map tile or autocomplete result is loaded, Google may set its own cookies, governed by Google's privacy policy.</li>
		<li><strong>OpenStreetMap tile servers</strong> — may set technical cookies governed by the OSM Foundation's privacy policy.</li>
	</ul>

	<p>
		We do not use Google Analytics, Facebook Pixel, or any similar
		profiling/advertising cookies.
	</p>

	<h3>3. Your choices</h3>
	<p>
		You can delete cookies through your browser settings or, on mobile, by
		clearing app storage. Disabling the authentication cookies will log you
		out and prevent you from using the Service.
	</p>

	<h3>4. Do Not Track</h3>
	<p>
		Some browsers send a "Do Not Track" signal. Because we do not engage in
		cross-site behavioural tracking, this signal has no additional effect on
		our Service.
	</p>

	<h3>5. Changes</h3>
	<p>
		If we add cookies that materially change how your data is used, we will
		update this Policy and, where required, ask for your express consent.
	</p>

