	<h2>Community Guidelines</h2>
	<p class="meta">Last updated: 18 May 2026</p>

	<p>
		Muse is a place to discover and share restaurants with the people you
		trust. Honest, kind and useful contributions are what make the feed worth
		opening. These Guidelines tell you what we expect from everyone using the
		Service and what will get content or accounts removed.
	</p>

	<h3>1. Be honest</h3>
	<ul>
		<li>Only rate restaurants you have actually visited.</li>
		<li>Don't accept payment or freebies in exchange for a positive rating.</li>
		<li>Don't post fake reviews for a competitor's venue or for your own.</li>
	</ul>

	<h3>2. Be respectful</h3>
	<ul>
		<li>No harassment, threats, hate speech, or attacks on the basis of race, ethnicity, national origin, religion, disability, gender, gender identity, sexual orientation, age or any other protected characteristic.</li>
		<li>Criticise the food, not the staff. Naming and shaming individual workers is not allowed.</li>
	</ul>

	<h3>3. Keep it legal</h3>
	<p>
		Do not post content that breaks the law, including Hong Kong laws on
		defamation, obscenity (Cap. 390), incitement, intellectual property
		infringement, or unsolicited commercial messages (Cap. 593). Doxxing —
		publishing private contact details of identifiable individuals — is
		prohibited and may also breach the Personal Data (Privacy) Ordinance
		(Cap. 486).
	</p>

	<h3>4. Respect privacy</h3>
	<ul>
		<li>Don't post photos of identifiable people without their consent.</li>
		<li>Don't share personal data (phone numbers, home addresses, ID numbers, financial information) of other people.</li>
		<li>Treat friends' shared activity as private to that friendship: don't republish it elsewhere without permission.</li>
	</ul>

	<h3>5. No spam, no scams</h3>
	<ul>
		<li>No bulk promotion, affiliate spam, or repeated unsolicited messages.</li>
		<li>No phishing, fake links, malware, or impersonation of Muse staff.</li>
	</ul>

	<h3>6. Adult content & safety</h3>
	<p>
		Pornographic or sexually explicit content, gratuitous violence, and content
		that sexualises minors are prohibited and will be reported to the relevant
		authorities. Content that promotes self-harm or suicide is not permitted;
		if you are in distress, please contact the Samaritans Hong Kong on
		2896&nbsp;0000.
	</p>

	<h3>7. Intellectual property</h3>
	<p>
		Only post photos and text you have the right to share. If you believe your
		copyright has been infringed, email
		<a href="mailto:info@lovemuse.app">info@lovemuse.app</a> with the URL or
		screenshot, identification of the work and a statement that you are the
		rights holder.
	</p>

	<h3>8. Reporting and enforcement</h3>
	<p>
		If you see content that violates these Guidelines, report it by emailing
		<a href="mailto:info@lovemuse.app">info@lovemuse.app</a>. We will review
		the report. Depending on severity and history, we may:
	</p>
	<ul>
		<li>remove the content;</li>
		<li>warn the user;</li>
		<li>suspend or terminate the account;</li>
		<li>report the matter to law enforcement when legally required.</li>
	</ul>

	<h3>9. Appeals</h3>
	<p>
		If you believe your content or account was actioned in error, reply to the
		notification email within 30 days. We will review the appeal and respond.
	</p>
